public class PerulannganWhile {
    public static void main(String[] args){
        int nilai = 1;
        while (nilai <= 10) {
            System.out.println("Angka: " + nilai);
            nilai++;
            System.out.println("\n");
        }
        int no = 1;
        do{
            System.out.println("Mahasiswa: "+ no);
            no++;
        } while(no <= 5);
    }
    
}
